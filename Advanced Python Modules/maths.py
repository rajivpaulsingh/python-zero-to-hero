import math

# help(math)
value = 4.35

# Rounding numbers
print(math.floor(value))
print(math.ceil(value))
print(round(4.35))

# Mathematical Constants
print(math.pi)
print(math.e)
print(math.tau)
print(math.inf)
print(math.nan)

# Logarithmic Values
print(math.e)
print(math.log(math.e))
print(math.log(10))
print(math.log(100, 10))

# Trigonometrics Functions
# Radians
print(math.sin(10))
print(math.degrees(math.pi/2))
print(math.radians(180))
